<script>
  import { loadData, loadCountries } from './tools/searchTools';
	import { beforeUpdate, afterUpdate, onMount } from 'svelte';
  import { nodes, links } from './__CONVERSATION__';
  import lunr from 'lunr';
  import lunrstemmer from 'lunr-languages/lunr.stemmer.support';
  import lunrfr from 'lunr-languages/lunr.fr';

  lunrstemmer(lunr)
  lunrfr(lunr)

	let div;
  let data;
  let countries;
	let autoscroll;
  let lunrIdx;
  let countriesIdx;
  let etat = {};

  const normaliseSpelling = function (builder) {
    // Define a pipeline function that remove accents
    var pipelineFunction = function (token) {
      return token
        .replace('á', 'a')
        .replace('é', 'e')
        .replace('í', 'i')
        .replace('ó', 'o')
        .replace('ú', 'u')
        .replace('Á', 'A')
        .replace('É', 'E')
        .replace('Í', 'I')
        .replace('Ó', 'O')
        .replace('Ú', 'U');
    }

    // Register the pipeline function so the index can be serialised
    lunr.Pipeline.registerFunction(pipelineFunction, 'normaliseSpelling')

    // Add the pipeline function to both the indexing pipeline and the
    // searching pipeline
    builder.pipeline.before(lunr.stemmer, pipelineFunction)
    builder.searchPipeline.before(lunr.stemmer, pipelineFunction)
  }

  onMount(async () => {
    data = await loadData()
    countries = await loadCountries()
    countriesIdx = lunr(function () {
      this.use(lunr.fr)
      this.ref('id')
      this.field('country')

      countries.forEach((doc, idx) => {
        doc.id = idx
        this.add(doc)
      }, this)
    })
    lunrIdx = lunr(function () {
      console.log(`loaded ${data.length}`);
      this.use(normaliseSpelling)
      this.use(lunr.fr)
      this.ref('id')
      this.field('question', {boost: 10})
      this.field('response')
      //this.field('responseGN')

      data.forEach((doc, idx) => {
        doc.id = idx
        this.add(doc)
      }, this)
    })
  })

	beforeUpdate(() => {
		autoscroll = div && (div.offsetHeight + div.scrollTop) > (div.scrollHeight - 20);
	});

	afterUpdate(() => {
		if (autoscroll) div.scrollTo(0, div.scrollHeight)
	});


  let state = {
    position: 0,
    end: 0,
    attribution: {
      child: null,
      comment: false
    }
  }

  state.position = nodes.find(x => x.id === 'start').id
  state.childs = links.filter(x => x.from == state.position).map(x => x.to)

	let comments = [
		{ auth: 'chatbot', text: 'Bonjour,' },
    { auth: 'chatbot', text: nodes.find(x => x.id === 'start').question },
    { auth: 'chatbot', options: state.childs.map(child => nodes.filter(x => child == x.id)).map(x => {
      return {text: x[0].text, id: x[0].id}
    }) }
	];

  const handleRules = async (option, idx) => {
    if (option.id) {
      comments = comments.slice(0, idx + 1)
      comments[idx].options.forEach(option => delete option.selected)
      option.selected = true

      state.position = nodes.find(x => x.id === option.id).id
      state.childs = links.filter(x => x.from == state.position).map(x => x.to)
      await handleAnswer(nodes.find(x => x.id === state.position).question)

      if (state.childs.length > 1) {
        comments = comments.concat({
          author: 'chatbot',
          options: state.childs.map(child => nodes.filter(x => child == x.id)).map(x => {
            return {text: x[0].text, id: x[0].id}
          })
        });
      } else if (state.childs.length === 1) {
        if (state.childs[0].includes('questions-vehicule')) {
          handleRules(nodes.find(x => state.childs[0] == x.id))
        }
      }
    }
  }

  const handleSubmit = (event) => {
    if (event.which === 13) {
      event.preventDefault();
      const text = event.target.value;
      if (!text) return;

      comments = comments.concat({
        author: 'user',
        text
      });

      const cleanText = text.replace('l\'','')

      const attributionRegex = /(attribution)/gim
      if (cleanText.match(attributionRegex) && (cleanText.match(attributionRegex).length > 0)) {
        state.attribution.comment = true
      }

      handleRules(state)

    }
  }

  const handleSearch = (searchTerm) => {
    if (searchTerm) {
      const results = lunrIdx.search(searchTerm)
      const filteredResults = results
        .filter(item => item.score > 0.9)
        .map(item => {
          console.log(item);
          return data[item.ref]
        })
      return filteredResults
    } else {
      return []
    }
  }

  const handleAnswer = (reply) => {
    return new Promise((resolve, reject) => {
      setTimeout(() => {
        comments = comments.concat({
          author: 'otheruser',
          text: '...',
          placeholder: true
        });

        setTimeout(() => {
          comments = comments.filter(comment => !comment.placeholder).concat({
            author: 'otheruser',
            text: reply
          });
          resolve(comments)
        }, 500 + Math.random() * 500);
      }, 200 + Math.random() * 200);
    })
  }

</script>

